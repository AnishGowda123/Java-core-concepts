public class primitivedata {
    public static void main(String[] args) {
        int a= 10;
        short b = 20;
        byte c= 7;
        float e= 10.5f;
        double f = 20;
        boolean g= true;
        System.out.println("int number is="+a);
        System.out.println("short number is="+b);
        System.out.println("byte number is="+c);
        System.out.println("float number is="+e);
        System.out.println("double number is="+f);
        System.out.println("boolean value is="+g);


    }
}



