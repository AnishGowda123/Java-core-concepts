/* In static we can declare variable without intialixation*/ 
public class without_intilization_static {
    static int a;
    static float b;
    static double c;
    static boolean d;
    static short e;
    public static void main(String[] args) {
        System.out.println(without_intilization_static.a);
        System.out.println(without_intilization_static.b);
        System.out.println(without_intilization_static.c);
        System.out.println(without_intilization_static.d);
        System.out.println(without_intilization_static.e);

    }
}
