/* In static we can declare variable without intialixation*/ 
public class basic_static_03 {
    static int a;
    static float b;
    static double c;
    static boolean d;
    static short e;
    public static void main(String[] args) {
        System.out.println(basic_static_03.a);
        System.out.println(basic_static_03.b);
        System.out.println(basic_static_03.c);
        System.out.println(basic_static_03.d);
        System.out.println(basic_static_03.e);

    }
}
