public class String_method6 {
    public static void main(String[] args) {
        String s = "java is easy language";
        String s1 = "sql is query language";
        String s2 = "WEB CONSIST OF HTML,CSS,JS";
        String s3 = "JDBC IS A API";
        System.out.println(s.toUpperCase());// toupper
        System.out.println(s1.toUpperCase());
        System.out.println("-------------------------");
        System.out.println(s2.toLowerCase());//tolower
        System.out.println(s3.toLowerCase());
    }
}
