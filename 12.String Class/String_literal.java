public class String_literal {
    public static void main(String[] args) {
        String s1="java";
        String s2="python";
        String s3="Java";
        String s4="java";
        System.out.println(s1==s2);
        System.out.println(s1==s3);
        System.out.println(s1==s4);
        System.out.println(s3==s1);
        System.out.println(s3==s4);
    }
}
