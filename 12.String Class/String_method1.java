public class String_method1 {
    public static void main(String[] args) {
        String s = "String";
        String s1 = "java";
        String s2 = "office word";
        System.out.println(s.length());// length method
        System.out.println(s1.length());
        System.out.println(s2.length());
        System.out.println("---------------------");
        System.out.println(s.charAt(2));// charAt method
        System.out.println(s1.charAt(3));
        System.out.println(s2.charAt(7));
    }
}
